import UIKit

class DeviceInfo {
    
    // Screen width.
    class var screenWidth: CGFloat {
        return UIScreen.main.bounds.width
    }
    
    // Screen height.
    class var screenHeight: CGFloat {
        return UIScreen.main.bounds.height
    }

}
